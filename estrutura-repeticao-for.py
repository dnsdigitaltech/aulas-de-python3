#Laço for percorre um conjuto de dados pré estabelecido
#variavel especial lista que são sequência de número, caracteres ou conjuto de dados

lista1 = [1,2,3,4,5]
lista2 = ["ola","mundo","!"]
lista3 = [0,"ola","biscoito","bolacha",9.99,True]

#usaremos o laço FOR para percorrer a nossa lista

for i in lista1:
    print(i)

for i in lista2:
    print(i)

for i in lista3:
    print(i)