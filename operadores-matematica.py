"""
+    adição
-    subtração
*    multiplicação
/    divisão
**   exponeciação
%    módulo
=    atribuição
"""
print(2 + 2) # adição

print(2 - 2) # subtração

print(2 / 2) # divisão

print(2 * 2) # multiplicação

#exponenciação 2 elevado a 3 = 2*2*2
print(2 ** 3)

print(10 % 3) # modulo resto de uma operação

var1 = 10  # atribuição
print(var1)